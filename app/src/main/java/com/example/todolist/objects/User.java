package com.example.todolist.objects;

public class User {

    private String name, email, password, _id, message;

    public String getMessage() {
        return message;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }

    public String get_id() {
        return _id;
    }

}
